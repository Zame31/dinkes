          Surabaya, eHealth. Selama tiga hari ini, mulai tanggal 28 â€“ 30 Juli 2016 menjadi hari yang menentukan bagi tiga Puskesmas di Kota Surabaya,
          yakni Puskesmas Jagir, Puskesmas Dupak dan juga Puskesmas Pucang Sewu.
          Pasalnya, tahun ini ketiga Puskesmas tersebut menjalani penilaian akreditasi Puskesmas oleh tim surveior dari Komisi Akreditasi Fasilitas Kesehatan Tingkat Pertama Kementerian Kesehatan RI. <br><br>

      Salah satu Puskesmas, yakni Puskesmas Dupak telah siap untuk menjalani akreditasi Puskesmas ini. Hal ini diutarakan oleh Kepala Puskesmas Dupak dr.
      Nurul Lailah saat menyampaikan presentasinya tentang garis besar kegiatan dan upaya Puskesmas dalam meningkatkan mutu dan kinerja Puskesmas Dupak di hadapan tim surveior saat pelaksanaan pembukaan acara di hari pertama penilaian akreditasi Puskesmas Dupak, hari Kamis (28/07/2016).<br><br>

      Dalam kesempatan ini pula, tim surveior Puskesmas Dupak yang terdiri dari tiga orang, yakni Sutedjo, SKM, M.Kes, Sri Waras, SKM, dr, Hj. Dwidia Mertasari, MPH juga memperkenalkan diri serta menyatakan maksud dan tujuannya untuk melakukan penilaian akreditasi di Puskesmas yang beralamatkan di
      Jl. Dupak Bangunrejo Gg. Poliklinik No. 6 Surabaya ini. <br><br>

      Dalam paparannya yang diwakili oleh Sutedjo, SKM, M.Kes, tujuannya datang ke Puskesmas Dupak untuk menilai kesesuaian penyelenggaraan Administrasi Manajemen (Admen),
       penyelenggaraan Upaya Kesehatan Masyarakat (UKM) dan juga Upaya Kesehatan Perseorangan (UKP) di Puskesmas dengan persyaratan yang ada pada standar akreditasi,
       menilai sejauh mana Puskesmas mampu menyelenggarakan pelayanan yang sesuai dengan persyaratan peraturan perundangan yang berlaku untuk pelayanan Puskesmas, dan juga menilai upaya perbaikan berkesinambungan yang dilakukan oleh Puskesmas dalam penyelenggaraan pelayanan.<br><br>

      Selama tiga hari, lanjut pria kelahiran Kota Kediri ini, tim surveior melaksanakan penilaian akreditasi sesuai dengan yang telah dijadwalkan.
      Pada hari pertama penilaian, tim surveior yang membagi peran tugas nya masing-masing, yakni Sutedjo pada Administasi Manajemen (Admen),
      Sri Waras pada UKM dan dr. Dwidia Mertasari pada UKP ini akan meminta daftar pasien rawat jalan maupun rawat inap dua bulan terakhir,
      telaah dokumen Admen, UKM dan UKP serta telusur sistim penyelenggaraan UKM dan juga pelayanan UKP.<br><br>
